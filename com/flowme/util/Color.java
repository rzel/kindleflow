	public final static byte getAlpha(int color) {
		return (byte) ((color >>> 24) & 0xFF); 
	}
	
	public final static byte getRed(int color) {
		return (byte) ((color >>> 16) & 0xFF); 
	}

	public final static byte getGreen(int color) {
		return (byte) ((color >>> 8) & 0xFF); 
	}

	public final static byte getBlue(int color) {
		return (byte) ((color >>> 0) & 0xFF); 
	}


